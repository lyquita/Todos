<script setup lang="ts">
import { ref } from "@vue/reactivity";

const emit = defineEmits(["addTodo"]);
const inputValue = ref("");

function submit() {
  emit("addTodo", inputValue);
  inputValue.value = "";
}
</script>

<template>
  <main class="flex fixed bottom-3 w-full justify-center">
    <textarea
      id="chat"
      v-model="inputValue"
      rows="1"
      class="block mx-5 p-2.5 w-full text-sm text-gray-900 bg-white rounded-lg border border-gray-300 focus:ring-blue-500 focus:border-blue-500 dark:bg-gray-800 dark:border-gray-600 dark:placeholder-gray-400 dark:text-white dark:focus:ring-blue-500 dark:focus:border-blue-500"
      placeholder="Add a new todo here..."
    ></textarea>
    <button
      @click="submit"
      class="inline-flex justify-center p-2 rounded-full cursor-pointer active:bg-[#FCD8D4] dark:text-blue-500 dark:hover:bg-gray-600"
    >
      <svg
        class="w-10 h-10 color-[#F5C6AA]"
        fill="none"
        stroke="currentColor"
        viewBox="0 0 24 24"
        xmlns="http://www.w3.org/2000/svg"
      >
        <path
          stroke-linecap="round"
          stroke-linejoin="round"
          stroke-width="1"
          d="M12 9v3m0 0v3m0-3h3m-3 0H9m12 0a9 9 0 11-18 0 9 9 0 0118 0z"
        ></path>
      </svg>
    </button>
  </main>
</template>
